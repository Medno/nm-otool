#include "lib_nm_otool.h"

uint32_t	convert_little_endian(uint32_t ptr)
{
	return (((unsigned)ptr << 24)
		+ ((unsigned)ptr >> 8 << 24 >> 8)
		+ ((unsigned)ptr >> 16 << 24 >> 16)
		+ ((unsigned)ptr >> 24 << 24 >> 24));
}
